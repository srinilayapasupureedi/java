package introTopics;

public class relational_operators {
    public static void main(String[] args) {
        int a=10,b=15;
        System.out.println(a==b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a!=b);
    }
}

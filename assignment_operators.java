package introTopics;

public class assignment_operators {
    public static void main(String[] args) {
        int age=25;
        int a=10;
        int val=age;
        System.out.println(val);
        val +=a;
        System.out.println(val);
        val -=a;
        System.out.println(val);
        val *=a;
        System.out.println(val);
        val /=a;
        System.out.println(val);
        val %=a;
        System.out.println(val);
    }
}
